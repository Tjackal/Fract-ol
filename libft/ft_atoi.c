int		ft_atoi(const char *str)
{
	unsigned long int		nb;
	int						s;

	nb = 0;
	s = 1;
	while (*str == '\r' || *str == '\v' || *str == '\f' ||
	*str == '\n' || *str == '\t' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			s = -1;
	while (*str >= '0' && *str <= '9')
	{
		nb *= 10;
		nb += (unsigned long int)(*str - '0');
		if ((nb > 9223372036854775807 || (nb == 922337203685477580
			&& (*str - '0') > 7)) && s == 1)
			return (-1);
		else if ((nb > 9223372036854775807 || (nb == 922337203685477580
			&& (*str - '0') > 8)) && s == -1)
			return (0);
		str++;
	}
	return ((int)(nb) * s);
}
