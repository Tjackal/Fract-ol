int		ft_sqrt(int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (nb >= 0)
	{
		while (j <= nb)
		{
			j = i * i;
			if (j == nb)
				return (i);
			i++;
		}
		return (0);
	}
	return (0);
}
